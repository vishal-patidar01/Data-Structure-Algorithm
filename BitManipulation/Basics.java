package BitManipulation;


// Bit-Wise Operators

// 1) Binary AND (&)
// 2) Binary OR (|)
// 3) Binary XOR (^)
// 3) Binary one's complement (~)
// 3) Binary left shift (<<)
// 3) Binary right shift (>>)


public class Basics {
    public static void main(String[] args) {

        // OR Operator

        int a = 5;
        int b = 6;

        int output = a & b;
        //System.out.println(output);

        // AND Operator

        output = a | b;
        //System.out.println(output);

        // XOR operator
        output = a ^ b;
        System.out.println(output);


    }
    
}
